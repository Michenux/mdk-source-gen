package com.a2a.adjava.xmodele;

public class MLabel {

	private String key;
	
	private String value;

	protected MLabel(String p_sKey, String p_sValue) {
		super();
		this.key = p_sKey;
		this.value = p_sValue;
	}

	public String getKey() {
		return key;
	}

	public void setKey(String key) {
		this.key = key;
	}

	public String getValue() {
		return value;
	}

	public void setValue(String value) {
		this.value = value;
	}
}
